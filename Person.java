/*
 *  Rapresents generic Person
 */
public class Person {
    final String name;
    final String surname;
    final String fiscalCode;
    final String phone;

    public Person(String name, String surname, String fiscalCode, String phone){
        this.name = name;
        this.surname = surname;
        this.fiscalCode = fiscalCode;
        this.phone = phone;
    }
}
